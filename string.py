# this is the demo in Emobilis explaining what a string is and how does it work
text = "hello world"
course = "WEB"
print(text)

# Accessing the element in a string
print(text[2])
print(text[0])

# returning the size of the string
print(len(text))

# modifying the string
print(text.upper())
print(course.lower())

# string concatenation  -this is combining or joining the string
first = "Hello"
second = "there"
print(first + " " + second)

